def should_skip(movie):
    tmdb_id, title, title_ru, year, rating, genre, description, description_ru = movie
    if not title and not title_ru:
        return True
    return False


def main():
    print(f"[{datetime.now().strftime('%H:%M:%S')}] Крейк запущен. Хранилище: PostgreSQL.")

    # PostgreSQL connection
    pg = psycopg2.connect(PG_URL)
    pg_cur = pg.cursor()

    # Create table if not exists
    pg_cur.execute("""
        CREATE TABLE IF NOT EXISTS ai_reviews (
            tmdb_id INTEGER,
            lang TEXT,
            review TEXT,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
            PRIMARY KEY (tmdb_id, lang)
        )
    """)
    pg.commit()

    # Load already-done tmdb_ids from PG into a set (fast local lookup)
    print("Загружаю список уже обработанных фильмов из PG...")
    pg_cur.execute("SELECT tmdb_id FROM ai_reviews WHERE lang='ru'")
    done_ids = set(row[0] for row in pg_cur.fetchall())
    already_done = len(done_ids)
    print(f"Уже в PG: {already_done} фильмов")

    # SQLite for movies
    movies_conn = sqlite3.connect(MOVIES_DB)

    total = movies_conn.execute("SELECT COUNT(*) FROM movies").fetchone()[0]
    print(f"Всего фильмов в SQLite: {total}")

    # Load all movies ordered by priority, filter out done ones in Python
    # (faster than repeated subqueries)
    print("Загружаю фильмы из SQLite...")
    all_movies = movies_conn.execute("""
        SELECT tmdb_id, title, title_ru, year, rating, genre, description, description_ru
        FROM movies
        WHERE (description IS NOT NULL AND description != '')
           OR (description_ru IS NOT NULL AND description_ru != '')
           OR (title_ru IS NOT NULL AND title_ru != '')
        ORDER BY
          CASE WHEN year = 2026 THEN 0
               WHEN year = 2025 THEN 1
               WHEN rating >= 7.0 THEN 2
               ELSE 3 END ASC,
          year DESC,
          rating DESC
    """).fetchall()
    print(f"Фильмов с данными: {len(all_movies)}")

    # Filter out already done
    todo = [m for m in all_movies if m[0] not in done_ids]
    print(f"Осталось обработать: {len(todo)}")

    batch_num = 0
    processed = 0
    skipped = 0
    start_time = time.time()
    last_review_sample = ""

    for i in range(0, len(todo), BATCH_SIZE):
        batch = todo[i:i + BATCH_SIZE]
        batch_num += 1
        batch_reviews = []
        batch_skipped = 0

        for movie in batch:
            if should_skip(movie):
                batch_skipped += 1
                skipped += 1
                continue

            tmdb_id = movie[0]
            seed = tmdb_id if tmdb_id else abs(hash(str(movie[1])))

            try:
                review_ru = generate_review_ru(movie, seed)
                review_en = generate_review_en(movie, seed)
                batch_reviews.append((tmdb_id, 'ru', review_ru))
                batch_reviews.append((tmdb_id, 'en', review_en))
                last_review_sample = review_ru[:200]
                processed += 1
            except Exception as e:
                print(f"  ERR tmdb_id={tmdb_id}: {e}")
                skipped += 1

        if batch_reviews:
            psycopg2.extras.execute_batch(
                pg_cur,
                """INSERT INTO ai_reviews (tmdb_id, lang, review) VALUES (%s, %s, %s)
                   ON CONFLICT (tmdb_id, lang) DO UPDATE SET review = EXCLUDED.review""",
                batch_reviews,
                page_size=100
            )
            pg.commit()

        total_done = already_done + processed
        elapsed = time.time() - start_time
        rate_per_sec = processed / elapsed if elapsed > 0 else 0
        rate_per_hour = rate_per_sec * 3600
        remaining = len(todo) - processed - skipped
        eta_min = (remaining / rate_per_sec / 60) if rate_per_sec > 0 else 0

        print(f"Батч {batch_num} готов: {total_done}/{total} фильмов | +{len(batch_reviews)//2} | ETA: {eta_min:.0f} мин")

        # Report every 1000 films
        if processed > 0 and processed % 1000 < BATCH_SIZE:
            pg_cur.execute("SELECT COUNT(*) FROM ai_reviews WHERE lang='ru'")
            ru_count = pg_cur.fetchone()[0]
            pg_cur.execute("SELECT COUNT(*) FROM ai_reviews WHERE lang='en'")
            en_count = pg_cur.fetchone()[0]
            pct = (total_done / total * 100) if total > 0 else 0
            print(f"""
╔══════════════════════════════════════════╗
║  ОТЧЁТ КАЖДЫЕ 1000 ФИЛЬМОВ
╠══════════════════════════════════════════╣
║  RU отзывов в PG:  {ru_count}
║  EN отзывов в PG:  {en_count}
║  Прогресс:    {total_done}/{total} ({pct:.1f}%)
║  Осталось:    {remaining}
║  Скорость:    {rate_per_hour:.0f} фильмов/час
╠══════════════════════════════════════════╣
║  Пример последнего RU:
║  {last_review_sample[:180]}
╚══════════════════════════════════════════╝
""")

    # Final
    pg_cur.execute("SELECT COUNT(*) FROM ai_reviews WHERE lang='ru'")
